      <h2 class="blog-title">PALAWAN: THE LAST FRONTIER OF THE PHILIPPINES</h2>

      <div class="main-image">
        <img src="/WDSL/Assets/Pictures/PH_Palawan.AVIF" alt="Palawan landscape">
      </div>

      <p>
        Visiting Palawan is like stepping into a tropical paradise where nature reigns supreme. 
        Crystal-clear waters, limestone cliffs, and vibrant marine life welcome you at every turn. 
        From the moment you arrive, the island’s serene atmosphere and natural beauty make you feel 
        like you’re in another world.
      </p>

      <p>
        Palawan is more than just beaches — it’s a blend of adventure, relaxation, and culture. 
        Whether island-hopping in El Nido, diving in Coron, or exploring the Underground River, 
        there’s always a new wonder waiting to be discovered.
      </p>

      <h3 id="el-nido"><strong>El Nido</strong></h3>
      <p>
        El Nido is famous for its dramatic limestone cliffs, hidden lagoons, and pristine beaches. 
        Go island-hopping, snorkel among colorful coral reefs, or simply relax on Nacpan Beach. 
        It’s an unforgettable experience for nature lovers and adventurers alike.
      </p>

      <h3 id="coron"><strong>Coron</strong></h3>
      <p>
        Known for its world-class wreck diving and crystal-clear lakes, Coron is perfect for 
        underwater exploration. Kayak through Kayangan Lake, or hike up Mt. Tapyas for a panoramic view 
        of the islands.
      </p>

      <h3 id="puerto-princesa"><strong>Puerto Princesa</strong></h3>
      <p>
        The capital city offers a mix of culture and nature. Explore the city’s charming streets, 
        local markets, and restaurants, or take a boat tour to nearby islands and snorkeling spots.
      </p>

      <h3 id="underground-river"><strong>Puerto Princesa Underground River</strong></h3>
      <p>
        One of the New 7 Wonders of Nature, this UNESCO World Heritage site features a stunning 
        subterranean river flowing through limestone caves. Guided tours let you marvel at its 
        impressive stalactites and stalagmites.
      </p>

      <h3 id="beaches"><strong>Beaches & Islands</strong></h3>
      <ul>
        <li>Honda Bay — island-hopping paradise with snorkeling</li>
        <li>Snake Island — famous for its unique sandbar</li>
        <li>Seven Commandos Beach — white sand and turquoise waters</li>
        <li>Port Barton — quiet and less crowded for relaxation</li>
      </ul>

      <h3 id="other"><strong>Other Recommendations</strong></h3>
      <ul>
        <li>Tubbataha Reefs Natural Park — diving hotspot and marine sanctuary</li>
        <li>Sabang Mangrove Forest — scenic kayaking through lush mangroves</li>
        <li>El Nido Sunset Tour — enjoy breathtaking sunsets on the islands</li>
        <li>Local seafood markets — taste fresh catch from the sea</li>
      </ul>

