      <h3 id="colosseum"><strong>Colosseum</strong></h3>
      <p>
        The Colosseum, Rome’s iconic amphitheater, is a must-see. 
        Marvel at its architecture, imagine the gladiatorial games, and learn about the rich history of ancient Rome.
      </p>

      <h3 id="vatican"><strong>Vatican City</strong></h3>
      <p>
        Home to the Pope, St. Peter’s Basilica, and the Vatican Museums, including the Sistine Chapel. 
        It’s a treasure trove of art, culture, and spiritual significance.
      </p>

      <h3 id="pantheon"><strong>Pantheon</strong></h3>
      <p>
        One of the best-preserved ancient Roman buildings, the Pantheon impresses with its massive dome and stunning architecture. 
        Visit during daylight to admire how sunlight filters through the oculus.
      </p>

      <h3 id="trevi"><strong>Trevi Fountain</strong></h3>
      <p>
        Toss a coin into this world-famous fountain to ensure your return to Rome. 
        Its baroque sculptures and flowing water make it a magical spot, especially at night.
      </p>

      <h3 id="piazzas"><strong>Famous Piazzas</strong></h3>
      <ul>
        <li>Piazza Navona — fountains and lively street performances</li>
        <li>Piazza Venezia — historic heart of the city</li>
        <li>Piazza del Popolo — great for evening strolls</li>
      </ul>

      <h3 id="other"><strong>Other Recommendations</strong></h3>
