      <h3 id="tips"><strong>Travel Tips</strong></h3>
      <ul>
        <li><strong>Getting Around:</strong> Ride-hailing apps like <em>Bolt</em> and <em>Uber</em> make it easy to move around the city.</li>
        <li><strong>Best Season:</strong> November to March — dry, cool, and perfect for sightseeing.</li>
        <li><strong>Local Food:</strong> Don’t miss out on <em>Suya</em> (spicy grilled meat), <em>Jollof Rice</em>, and <em>Pounded Yam</em>.</li>
        <li><strong>Etiquette:</strong> Nigerians are warm and friendly — always greet with a smile and respect local customs.</li>
      </ul>

      <p>
        Abuja’s balance of culture, nature, and modernity makes it one of Africa’s most underrated destinations. 
        From tranquil lakes to striking architecture, every corner of this city tells a story — 
        one of progress, pride, and passion.
      </p>

