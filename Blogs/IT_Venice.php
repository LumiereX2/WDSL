      <h2 class="blog-title">VENICE: THE CITY OF CANALS</h2>

      <div class="main-image">
        <img src="/WDSL/Assets/Pictures/IT_Venice.webp" alt="Venice canals">
      </div>

      <p>
        Venice is a city unlike any other, built on a network of canals and full of charming bridges, 
        historic architecture, and vibrant culture. Its romantic atmosphere makes it a dream destination.
      </p>

      <h3 id="grand-canal"><strong>Grand Canal</strong></h3>
      <p>
        The Grand Canal is the main waterway of Venice. Ride a vaporetto or gondola to admire 
        the beautiful palaces and historic buildings lining the canal.
      </p>

      <h3 id="st-marks"><strong>St. Mark's Square</strong></h3>
      <p>
        Known as Piazza San Marco, it’s the heart of Venice. Visit St. Mark’s Basilica, 
        climb the Campanile for views, and enjoy the cafes surrounding the square.
      </p>

      <h3 id="rialto"><strong>Rialto Bridge</strong></h3>
      <p>
        One of Venice’s most famous landmarks, the Rialto Bridge spans the Grand Canal 
        and is surrounded by markets and shops.
      </p>

      <h3 id="gondola"><strong>Gondola Rides</strong></h3>
      <p>
        A gondola ride is a must-do experience. Glide through narrow canals and picturesque streets 
        while enjoying the serenity of Venice from the water.
      </p>

      <h3 id="islands"><strong>Murano & Burano</strong></h3>
      <ul>
        <li>Murano — famous for glassmaking workshops</li>
        <li>Burano — colorful houses and lace-making tradition</li>
        <li>Torcello — peaceful island with historical sites</li>
      </ul>

      <h3 id="other"><strong>Other Recommendations</strong></h3>
      <ul>
        <li>Teatro La Fenice — Venice’s iconic opera house</li>
        <li>Jewish Ghetto — historic district with museums</li>
        <li>Campo Santa Margherita — lively square with cafes</li>
      </ul>

