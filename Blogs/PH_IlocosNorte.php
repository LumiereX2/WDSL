      <p class="blog-description">
        Last 2024, my family and I set out on a memorable journey to the northern part of the Philippines — Ilocos Norte and the historic city of Vigan. It was a trip filled with long drives, breathtaking views, and a deeper appreciation for the country’s rich heritage.
      </p>

      <h3 id="pagudpud">Pagudpud: The Northern Coast’s Calm Escape</h3>
      <p class="blog-body">
        Our adventure began in <strong>Ilocos Norte</strong>, where the endless coastline of <strong>Pagudpud</strong> welcomed us with its cool breeze and crystal-clear waters. The sight of the blue sea stretching into the horizon instantly washed away our travel fatigue. We spent hours just soaking in the sun, letting the sound of the waves and the laughter of my family fill the day.
      </p>

