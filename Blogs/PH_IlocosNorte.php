      </div>

      <h3 id="windmills">Bangui Windmills: Giants of the North</h3>
      <p class="blog-body">
        Not far from Pagudpud, the towering <strong>Bangui Windmills</strong> stood proudly against the coastal landscape. Seeing them up close was surreal — massive white blades spinning gracefully, harnessing the power of the wind. It felt like standing beside gentle giants, symbols of both progress and sustainability. We couldn’t resist taking photos at every angle; the windmills were just too stunning to pass up.
      </p>

      <div class="gallery">
        <img src="/WDSL/Assets/Pictures/PH_IlocosWindmills.jpg" alt="Bangui Windmills close-up">
      </div>

      <h3 id="vigan">Vigan City: Where Time Stands Still</h3>
      <p class="blog-body">
        After exploring the natural wonders of Ilocos Norte, we traveled south to <strong>Vigan City</strong>, a place that seemed frozen in time. The cobblestone streets, antique houses, and <em>calesas</em> moving slowly under the afternoon sun brought a sense of nostalgia, as if history itself was alive around us. Walking along <strong>Calle Crisologo</strong>, we admired the well-preserved Spanish-era architecture and even bought a few souvenirs from the local shops.
      </p>

      <div class="gallery">
        <img src="/WDSL/Assets/Pictures/PH_Vigan.jpg" alt="Calle Crisologo at night">

      </div>

      <h3 id="baluarte">Baluarte: Family Fun and Colors</h3>
      <p class="blog-body">
        One of the highlights of our visit was <strong>Baluarte</strong>, a unique park and zoo owned by former governor Chavit Singson. There, we saw various animals, took pictures with the colorful surroundings, and enjoyed a relaxing afternoon before heading back to town.
      </p>

      <div class="gallery">
        <img src="/WDSL/Assets/Pictures/PH_Baluarte.jpg" alt="Baluarte zoo">
       
      </div>

      <h3 id="reflections">Reflections</h3>
      <p class="blog-body">
        Our Ilocos trip was more than just sightseeing — it was about discovering culture, appreciating nature, and creating memories together as a family. From the cool winds of Pagudpud to the historic charm of Vigan, every stop offered something special. It was a reminder that traveling isn’t just about reaching destinations — it’s about sharing experiences and stories that stay with us long after the journey ends.
      </p>

