      <h1 class="blog-title">Ilocos Norte & Vigan: A Journey Through Time and Wind</h1>
    

      <div class="main-image">
        <img src="/WDSL/Assets/Pictures/PH_Ilocos.jpg" alt="Ilocos Norte">
